package Optimisation;

public class HelloWorld
{
    public static void main(String []args)
    {
        int maxEval = 100;
        int currentEval = 0;
        
        while (currentEval < maxEval)
        {
       System.out.println(currentEval);
       currentEval++;
        }
       System.out.println("Hello World");
    }
}
